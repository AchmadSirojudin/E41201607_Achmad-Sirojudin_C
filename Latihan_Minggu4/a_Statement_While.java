package Latihan_Minggu4;

public class a_Statement_While {

    public static void main(String[] args){
        int i = 0;

        while (i <= 10) {
//      tampilkan code yang akan diulang
            System.out.println("Ini adalah perulangan ke-" + i);
//      increment nilai i
            i++;
        }
    }
}
