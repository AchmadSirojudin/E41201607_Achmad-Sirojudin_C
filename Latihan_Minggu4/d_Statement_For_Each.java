package Latihan_Minggu4;

public class d_Statement_For_Each {

    public static void main(String[] args) {

//  Membuat Array
        int angka [] = {3,1,42,24,12};
//  Menggunakan perulangan for each untuk menampilkan angka
        for (int x : angka) {
            System.out.print(x + " ");
        }
    }
}
