package Latihan_Minggu4;

public class b_Statement_Do_While {

    public static void main(String[] args) {

//        Membuat variabel i
        int i = 0;
//        Tampilkan code yang akan dibuat perulangan
        do {
            System.out.println("Ini adalah perulangan ke-" + i);
            i++;
//        Kondisi Perulangan
        } while (i <= 10);

    }
}
